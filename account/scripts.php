<script>
$(".sidebar-dropdown > a").click(function() {
  $(".sidebar-submenu").slideUp(200);
  if (
    $(this)
      .parent()
      .hasClass("active")
  ) {
    $(".sidebar-dropdown").removeClass("active");
    $(this)
      .parent()
      .removeClass("active");
  } else {
    $(".sidebar-dropdown").removeClass("active");
    $(this)
      .next(".sidebar-submenu")
      .slideDown(200);
    $(this)
      .parent()
      .addClass("active");
  }
});

$("#close-sidebar").click(function() {
  $(".page-wrapper").removeClass("toggled");
});
$("#show-sidebar").click(function() {
  $(".page-wrapper").addClass("toggled");
});

</script>
<script>
$(document).ready(function(){
  $('.play-music').click(function(){
    document.querySelector('.play-music').style.display = "none";
    document.querySelector('.pause-music').style.display = "inline-block";
  });
});
$(document).ready(function(){
  $('.pause-music').click(function(){
    document.querySelector('.play-music').style.display = "inline-block";
    document.querySelector('.pause-music').style.display = "none";
  });
});
</script>